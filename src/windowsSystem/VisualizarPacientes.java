    private void lblEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblEditarMouseClicked
        // TODO add your handling code here:

        int fila= tblPacientes.getSelectedRow();
        String id= String.valueOf(tblPacientes.getValueAt(fila, 5));

        try {

            String nombre= String.valueOf(tblPacientes.getValueAt(fila, 0));
            String edad= String.valueOf(tblPacientes.getValueAt(fila, 1));
            String fechaN= String.valueOf(tblPacientes.getValueAt(fila, 2));
            String afiliacion= String.valueOf(tblPacientes.getValueAt(fila, 3));
            String telefono= String.valueOf(tblPacientes.getValueAt(fila, 4));
            String estado= String.valueOf(tblPacientes.getValueAt(fila, 6));

            PreparedStatement pps= ConexionBDD.conexion.prepareStatement("UPDATE pacientes SET nombre= '"+nombre+"', edad= '"+edad+"', fecha_nacimiento= '"+fechaN+"', afiliacion= '"+afiliacion+"', telefono= '"+telefono+"',no_control= '"+id+"' , estado_actual= '"+estado+"' WHERE no_control='"+id+"';");
            pps.executeUpdate();
            JOptionPane.showMessageDialog(this, "DATOS ACTUALIZADOS! =D");

        }
        catch (SQLException ex) {

            JOptionPane.showMessageDialog(this, "No se pudieron editar los datos");

        }

    }//GEN-LAST:event_lblEditarMouseClicked

