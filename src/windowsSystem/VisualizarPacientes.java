    public void mostrarDatosCliente(){
        
        tableModel= null;
        
        tableModel=new DefaultTableModel();
        tableModel.addColumn("Nombre");
        tableModel.addColumn("Edad");
        tableModel.addColumn("Fecha de nacimiento");
        tableModel.addColumn("Afiliación");
        tableModel.addColumn("Número de control");
        tableModel.addColumn("Telefóno");
        tableModel.addColumn("Estado actual");
        
        tblPacientes.setModel(tableModel);
        
        String [] datosBDD= new String[7];
        
        try{
            
            Statement stm= ConexionBDD.conexion.createStatement();
            ResultSet rs= stm.executeQuery("SELECT * FROM pacientes;");
            
            while(rs.next()){

                datosBDD[0]= rs.getString(1);
                datosBDD[1]= rs.getString(2);
                datosBDD[2]= rs.getString(3);
                datosBDD[3]= rs.getString(4);
                datosBDD[4]= rs.getString(5);
                datosBDD[5]= rs.getString(6);
                datosBDD[6]= rs.getString(7);
                
                tableModel.addRow(datosBDD);
                
                tblPacientes.setModel(tableModel);
                
            }
            
        }catch(Exception ev){
            
            JOptionPane.showMessageDialog(this, "LOS DATOS NO FUERON POSIBLES DE CARGAR");
            
        }
        
    }

