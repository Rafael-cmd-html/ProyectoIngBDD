    private void txtBuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyPressed
        // TODO add your handling code here:
        String [] registros= new String[200];
        String [] titulos= {"Nombre","Edad","Fecha de nacimiento","Afiliacion","Telefono","Estado Actual"};
        String sql;
        if(txtBuscar.getText().equals("")){

            sql= "SELECT * FROM pacientes;";

        }else{

            sql= "SELECT * FROM pacientes WHERE nombre LIKE '%"+txtBuscar.getText()+"%';";

        }
        DefaultTableModel tM= new DefaultTableModel(null, titulos);
        try{

            Statement st= ConexionBDD.conexion.createStatement();
            ResultSet rs= st.executeQuery(sql);

            while(rs.next()){

                registros[0]=rs.getString(1);
                registros[1]=rs.getString(2);
                registros[2]=rs.getString(3);
                registros[3]=rs.getString(4);
                registros[4]=rs.getString(5);
                registros[5]=rs.getString(6);
                registros[6]=rs.getString(7);
                tM.addRow(registros);

            }
            tblPacientes.setModel(tM);

        } catch (SQLException ex) {

            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "No se encontró el dato");

        }
    }//GEN-LAST:event_txtBuscarKeyPressed

